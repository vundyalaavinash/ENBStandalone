    protected static void addStylesToDocument(StyledDocument doc) {
        //Initialize some styles.
        javax.swing.text.Style def = StyleContext.getDefaultStyleContext().
                        getStyle(StyleContext.DEFAULT_STYLE);

        javax.swing.text.Style regular = doc.addStyle("regular", def);
        StyleConstants.setFontFamily(def, "Arial");

        javax.swing.text.Style s = doc.addStyle("italic", regular);
        StyleConstants.setItalic(s, true);

        s = doc.addStyle("bold", regular);
        StyleConstants.setBold(s, true);
        //StyleConstants.set

        s = doc.addStyle("small", regular);
        StyleConstants.setFontSize(s, 12);

        s = doc.addStyle("large", regular);
        StyleConstants.setFontSize(s, 16);

        s = doc.addStyle("icon", regular);
        StyleConstants.setAlignment(s, StyleConstants.ALIGN_CENTER);
        
        s = doc.addStyle("strike", regular);
        StyleConstants.setStrikeThrough(s, true);
        
        s = doc.addStyle("highlight", regular);
        StyleConstants.setBackground(s, Color.green);
        
        s = doc.addStyle("light", regular);
        StyleConstants.setBackground(s, Color.white);

        //Style parent = jTextPane1.addStyle("parent", jTextPane1.getStyle("default"));
        //StyleConstants.setBackground(parent, Color.red);
        //StyleConstants.setForeground(parent, Color.blue);
    }
    
