    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String filename;
        javax.swing.JFileChooser fc = new javax.swing.JFileChooser();
        javax.swing.filechooser.FileNameExtensionFilter picture = new javax.swing.filechooser.FileNameExtensionFilter("JPEG files (*.png)", "png");
        fc.setFileFilter(picture);
        fc.setFileSelectionMode(javax.swing.JFileChooser.FILES_AND_DIRECTORIES);

        if (fc.showDialog(this, "Insert")!=javax.swing.JFileChooser.APPROVE_OPTION) {
            return;
        }
        filename = fc.getSelectedFile().getAbsolutePath();

        // If no text is entered for the file name, refresh the dialog box
        if (filename==null) {
            return;
        }

        try
        {
            java.awt.image.BufferedImage img = javax.imageio.ImageIO.read(new File(filename));
            java.awt.Image newimg = img.getScaledInstance( 150, 200,  java.awt.Image.SCALE_SMOOTH ) ;
            javax.swing.ImageIcon pictureImage = new javax.swing.ImageIcon(newimg);
            //pictureImage.paintIcon(fc, null, 25, 25);
            //pictureImage.getIconWidth();
            jTextPane1.insertIcon(pictureImage);

        }

        catch (IOException e)
        {
            JOptionPane.showMessageDialog(null, "Could not find file: " + filename);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

